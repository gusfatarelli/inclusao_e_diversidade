/*
 * Trigger 1: Automação de efetivação por mérito social
 * Quando a vaga é atualizada para PREENCHIDA, seleciona o candidato com maior
 * score e o insere em TB_COLABORADORES.
 */
CREATE OR REPLACE TRIGGER TRG_CONTRATACAO_DIVERSIDADE
AFTER UPDATE OF STATUS ON TB_VAGAS
FOR EACH ROW
WHEN (NEW.STATUS = 'PREENCHIDA')
DECLARE
    v_nome_candidato TB_CANDIDATOS.NOME%TYPE;
BEGIN
    SELECT NOME INTO v_nome_candidato
    FROM (
        SELECT NOME FROM TB_CANDIDATOS
        WHERE FK_VAGA = :NEW.ID_VAGA
        ORDER BY SCORE_DIVERSIDADE DESC
    )
    WHERE ROWNUM = 1;
    INSERT INTO TB_COLABORADORES (ID_COLAB, NOME, FK_DEP)
    VALUES (SEQ_COLAB.NEXTVAL, v_nome_candidato, :NEW.FK_DEP);
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        NULL;
END;
/

/*
 * Trigger 2: Priorização de candidatos afirmativos
 * Bônus de 2 pontos no score para candidatos em vagas afirmativas.
 */
CREATE OR REPLACE TRIGGER TRG_PRIORIDADE_CANDIDATO
BEFORE INSERT ON TB_CANDIDATOS
FOR EACH ROW
DECLARE
    v_flag_afirmativa TB_VAGAS.FLAG_AFIRMATIVA%TYPE;
BEGIN
    SELECT FLAG_AFIRMATIVA INTO v_flag_afirmativa
    FROM TB_VAGAS WHERE ID_VAGA = :NEW.FK_VAGA;
    IF v_flag_afirmativa = 'S' THEN
        :NEW.SCORE_DIVERSIDADE := LEAST(NVL(:NEW.SCORE_DIVERSIDADE, 0) + 2, 10);
    END IF;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20001, 'Erro: vaga não encontrada para o candidato.');
END;
/

/*
 * Trigger 3: Matrícula automática em treinamento obrigatório
 * Após inserir colaborador, insere log de treinamento.
 */
CREATE OR REPLACE TRIGGER TRG_MATRICULA_COMPLIANCE
AFTER INSERT ON TB_COLABORADORES
FOR EACH ROW
BEGIN
    INSERT INTO TB_TREINAMENTOS_LOG (ID_LOG, FK_COLAB, TIPO_TREINAMENTO, DATA_CONCLUSAO)
    VALUES (SEQ_TREINAMENTO_LOG.NEXTVAL, :NEW.ID_COLAB, 'INTEGRAÇÃO E CULTURA INCLUSIVA', NULL);
EXCEPTION
    WHEN OTHERS THEN
        RAISE_APPLICATION_ERROR(-20001, 'Erro ao matricular colaborador no treinamento obrigatório: ' || SQLERRM);
END;
/

/*
 * Trigger 4: Bloqueio de vagas não inclusivas
 * Impede inserir vagas com FLAG_AFIRMATIVA = 'N' se o departamento tiver meta < 0.10.
 */
CREATE OR REPLACE TRIGGER TRG_BLOQUEIA_VAGA_NAO_INCLUSIVA
BEFORE INSERT ON TB_VAGAS
FOR EACH ROW
DECLARE
    v_meta TB_DEPARTAMENTOS.META_DIVERSIDADE%TYPE;
BEGIN
    IF :NEW.FLAG_AFIRMATIVA = 'N' THEN
        SELECT META_DIVERSIDADE INTO v_meta FROM TB_DEPARTAMENTOS WHERE ID_DEP = :NEW.FK_DEP;
        IF v_meta < 0.10 THEN
            RAISE_APPLICATION_ERROR(-20001, 'Abertura de vaga bloqueada: meta de diversidade do departamento abaixo de 10%.');
        END IF;
    END IF;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20002, 'Departamento não encontrado.');
END;
